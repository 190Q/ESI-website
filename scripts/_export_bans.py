def _print_python(blacklist, active):
    schema_lines = ",\n".join("    " + repr(s) for s in SCHEMA_STATEMENTS)
    blacklist_lines = "\n".join("    " + repr(r) + "," for r in blacklist) or "    # (empty)"
    active_lines = "\n".join("    " + repr(r) + "," for r in active) or "    # (empty)"

    sys.stdout.write(_PY_HEADER.format(name="create_bans.py"))
    sys.stdout.write(
        _PY_BODY.format(
            schema=schema_lines,
            blacklist=blacklist_lines,
            active=active_lines,
        )
    )


HELP_TEXT = """\
Usage: _export_bans.py <ip_bans.db> <pretty|sql|python>

Helper for scripts/ban-ip.sh list — prints the ip_bans.db contents.

Arguments:
  <ip_bans.db>    Path to the ip_bans.db SQLite file.
  <format>        Output format. One of:
                    pretty  Human-readable summary (default used by ban-ip.sh).
                    sql     Paste-ready INSERT statements for a matching DB.
                    python  Standalone Python script that (re)creates the DB.

Options:
  -h, --help      Show this help message and exit.
"""


def main() -> int:
    if any(arg in ("-h", "--help") for arg in sys.argv[1:]):
        print(HELP_TEXT)
        return 0
    if len(sys.argv) != 3:
        print("Usage: _export_bans.py <ip_bans.db> <pretty|sql|python>", file=sys.stderr)
        print("Run '_export_bans.py --help' for more information.", file=sys.stderr)
        return 2
    db_path, fmt = sys.argv[1], sys.argv[2]
    blacklist, active, now = _load(db_path)

    if fmt == "pretty":
        if not os.path.exists(db_path):
            print("  No ip_bans.db found.")
            return 0
        _print_pretty(blacklist, active, now)
    elif fmt == "sql":
        _print_sql(blacklist, active)
    elif fmt == "python":
        _print_python(blacklist, active)
    else:
        print(f"  Unknown format: {fmt} (use: pretty, sql, python)", file=sys.stderr)
        return 1
    return 0


if __name__ == "__main__":
    sys.exit(main())
