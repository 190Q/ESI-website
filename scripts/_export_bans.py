#!/usr/bin/env python3
"""Helper for scripts/ban-ip.sh list — prints the ip_bans.db contents.

Formats
-------
    pretty  Human-readable summary (default).
    sql     Paste-ready INSERT statements for a DB with the same schema.
    python  Standalone Python script that (re)creates the full DB from
            scratch, schema + data. Run it like:
                python3 create_bans.py [target.db]

Usage
-----
    python3 _export_bans.py <ip_bans.db path> <format>
"""
from __future__ import annotations

import os
import sqlite3
import sys
from time import time


# ─── SQL schema (kept in sync with ip_ban.py) ─────────────────────────────
SCHEMA_STATEMENTS = [
    """
    CREATE TABLE IF NOT EXISTS active_bans (
        ip          TEXT PRIMARY KEY,
        expires_at  REAL NOT NULL,
        jail        TEXT NOT NULL,
        banned_at   REAL NOT NULL,
        ban_count   INTEGER NOT NULL DEFAULT 1
    )
    """,
    """
    CREATE TABLE IF NOT EXISTS ban_history (
        id          INTEGER PRIMARY KEY AUTOINCREMENT,
        ip          TEXT    NOT NULL,
        jail        TEXT    NOT NULL,
        banned_at   REAL    NOT NULL,
        expires_at  REAL    NOT NULL,
        ban_count   INTEGER NOT NULL DEFAULT 1
    )
    """,
    """
    CREATE TABLE IF NOT EXISTS blacklist (
        ip          TEXT PRIMARY KEY,
        reason      TEXT NOT NULL DEFAULT '',
        added_at    REAL NOT NULL
    )
    """,
    "CREATE INDEX IF NOT EXISTS idx_bh_ip ON ban_history(ip)",
    "CREATE INDEX IF NOT EXISTS idx_bh_ts ON ban_history(banned_at)",
]


def _q(value) -> str:
    """Quote *value* as a single-quoted SQL literal."""
    return "'" + str(value).replace("'", "''") + "'"


def _load(db_path: str):
    now = time()
    if not os.path.exists(db_path):
        return [], [], now

    conn = sqlite3.connect(db_path)
    conn.row_factory = sqlite3.Row
    try:
        bl_rows = conn.execute(
            "SELECT ip, reason, added_at FROM blacklist ORDER BY added_at"
        ).fetchall()
    except sqlite3.OperationalError:
        bl_rows = []
    try:
        ban_rows = conn.execute(
            "SELECT ip, expires_at, jail, banned_at, ban_count"
            " FROM active_bans WHERE expires_at > ? ORDER BY expires_at",
            (now,),
        ).fetchall()
    except sqlite3.OperationalError:
        ban_rows = []
    conn.close()

    blacklist = [
        (r["ip"], (r["reason"] or ""), float(r["added_at"] or 0))
        for r in bl_rows
    ]
    active = [
        (
            r["ip"],
            float(r["expires_at"] or 0),
            r["jail"] or "manual",
            float(r["banned_at"] or 0),
            int(r["ban_count"] or 1),
        )
        for r in ban_rows
    ]
    return blacklist, active, now


def _print_pretty(blacklist, active, now):
    if not blacklist and not active:
        print("  No active bans or blacklisted IPs.")
        return
    if blacklist:
        print(f"  Blacklisted ({len(blacklist)}):")
        for ip, reason, _added_at in blacklist:
            print(f"    ● {ip:>15}  {reason or '–'}")
    if active:
        print(f"  Temp-banned ({len(active)}):")
        for ip, expires_at, jail, _banned_at, _count in sorted(
            active, key=lambda e: e[1] - now
        ):
            r = max(0, int(expires_at - now))
            m, s = divmod(r, 60)
            h, m = divmod(m, 60)
            print(f"    ○ {ip:>15}  {h}h {m}m {s}s left  ({jail})")


def _print_sql(blacklist, active):
    print("-- ip_bans.db dump (paste into sqlite3 target.db)")
    print("BEGIN TRANSACTION;")
    if blacklist:
        print(f"-- {len(blacklist)} blacklist entries")
        for ip, reason, added_at in blacklist:
            print(
                "INSERT OR REPLACE INTO blacklist (ip, reason, added_at) VALUES ("
                f"{_q(ip)}, {_q(reason)}, {added_at!r});"
            )
    if active:
        print(f"-- {len(active)} active temp bans")
        for ip, expires_at, jail, banned_at, count in active:
            print(
                "INSERT OR REPLACE INTO active_bans"
                " (ip, expires_at, jail, banned_at, ban_count) VALUES ("
                f"{_q(ip)}, {expires_at!r}, {_q(jail)}, {banned_at!r}, {int(count)});"
            )
    if not blacklist and not active:
        print("-- (nothing to dump)")
    print("COMMIT;")


# ─── Python-script output ─────────────────────────────────────────────────

